// 1. Create an array of numbers
let numbers = [10, 20, 30, 40, 50, 60, 70, 80];
// use the filter method to create a new array containing only greater than 50
let filteredNumber = numbers.filter( element => element >= 50);
// log the result
console.log(filteredNumber);


// 2. create an array of strings
let word = ["Hello", "Goodbye", "Hi", "Hey", "Yo"];
// use the filter method to create a new array containing only strings with less than 3 characters.
let filteredWord = word.filter( element => element.length <= 3 );
// log the result
console.log(filteredWord);

