// 1. Create an array of numbers and find the sum of all the elements.
let numbers = [1, 2, 3, 4, 5];
let sum = 0;
for (let i = 0; i < numbers.length; i++) {
    sum += numbers[i];
}
console.log("1. Sum of all elements:", sum);

// 2. Create an array of numbers and find the average of all the elements.
let numArray = [10, 20, 30, 40, 50];
let total = 0;
for (let i = 0; i < numArray.length; i++) {
    total += numArray[i];
}
let average = total / numArray.length;
console.log("2. Average of all elements:", average);
