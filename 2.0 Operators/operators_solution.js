// 1. Declare two variables and perform addition, subtraction, multiplication, division, and modulo operations on them.
let a = 10;
let b = 3;

console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulo:", a % b);

// 2. Declare a variable and use a compound assignment operator to add 5 to its value.
let num = 10;
num += 5;
console.log("Compound Assignment:", num);

// 3. Compare two variables and log the result.
let p = 5;
let q = "5";

console.log("Comparison (==):", p == q); // Loose equality
console.log("Comparison (===):", p === q); // Strict equality
console.log("Comparison (!=):", p != q);
console.log("Comparison (!==):", p !== q);
console.log("Comparison (>):", p > 3);
console.log("Comparison (>=):", p >= 5);
console.log("Comparison (<):", p < 8);
console.log("Comparison (<=):", p <= 5);

// 4. Perform logical AND, OR, and NOT operations on boolean variables.
let isAdult = true;
let hasLicense = false;

console.log("Logical AND:", isAdult && hasLicense);
console.log("Logical OR:", isAdult || hasLicense);
console.log("Logical NOT:", !isAdult);

// 5. Use unary operators to increment and decrement a variable.
let n = 5;

console.log("Increment:", ++n);
console.log("Decrement:", --n);

// 6. Use the conditional operator to compare two numbers and log the total.
let x = 10;
let y = 5;
let total = (x > y) ? "x is greater than y" : "x is not greater than y";
console.log("Conditional Operator:", total);

